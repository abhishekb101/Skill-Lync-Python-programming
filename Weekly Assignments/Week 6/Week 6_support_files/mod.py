"""Calculating the result of basic arithmetic operations between two numbers"""

def f1(x, y):
    """
    Returns the addition between two valid numbers
    """
    return x + y

def f2(x, y):
    """
    Returns the value of subtracting y from x
    """
    return x - y

def f3(x, y):
    """
    Returns the value of multiplication between two valid numbers
    """
    return x * y